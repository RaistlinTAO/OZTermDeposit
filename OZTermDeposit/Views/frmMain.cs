        private IEnumerable<sTermDepositDetail> CheckInfo(string URL)
        {
            try
            {
                var wc = new WebClient { Credentials = CredentialCache.DefaultCredentials };
                // 创建WebClient实例提供向URI 标识的资源发送数据和从URI 标识的资源接收数据
                // 获取或设置用于对向 Internet 资源的请求进行身份验证的网络凭据。
                Byte[] pageData = wc.DownloadData(URL);
                string iResult = Encoding.Default.GetString(pageData);

                //这里正则表达式
                string iRest = null;
                try
                {
                    iRest = Regex.Match(iResult, "<tbody>.*?</tbody>", RegexOptions.Singleline).Value;
                }
                catch (ArgumentException)
                {
                    // Syntax error in the regular expression
                }

                DelegateGetDetail dn = GetDetail;

                IAsyncResult iar = dn.BeginInvoke(iRest, null, null);

                return dn.EndInvoke(iar);
            }
            catch (Exception)
            {
                return null;
            }
            
        }



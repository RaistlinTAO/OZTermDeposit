        private IEnumerable<sTermDepositDetail> GetDetail(string tableData)
        {
            var iList = new List<sTermDepositDetail>();

            try
            {
                var regexObj = new Regex("<tr>.*?</tr>", RegexOptions.Singleline);
                Match matchResult = regexObj.Match(tableData);
                while (matchResult.Success)
                {
                    //Console.Write(matchResult.Value);

                    //处理一家机构的多个数值 
                    if (!matchResult.Value.Contains("<th class=\"company\""))
                    {
                        if (!matchResult.Value.Contains("<th class=\"center\">nom</th>"))
                        {
                            iList.Add(GetOneDetail(matchResult.Value));
                        }
                    }


                    //iList.Add();
                    matchResult = matchResult.NextMatch();
                }
            }
            catch (ArgumentException)
            {
                // Syntax error in the regular expression
            }


            return iList;
        }

        private sTermDepositDetail GetOneDetail(string iCompany)
        {
            float[] iRest = new float[14];
            string[] iFirst = new string[14];
            string[] iSecond = new string[14];
            sTermDepositDetail iDetail = new sTermDepositDetail();
            string iTemp = Regex.Match(iCompany, "<td class=\"company\">.*?</a></td>", RegexOptions.Singleline).Value;
            iDetail.iInst = iTemp.Split('"')[5];
            iDetail.iURL = iTemp.Split('"')[3];


            Regex regexObj = new Regex("<td class=\"center\">.*?</td>", RegexOptions.Singleline);
            Match iResult = regexObj.Match(iCompany);
            int i = 0;
            while (iResult.Success)
            {
                iTemp = iResult.Value.Replace("<td class=\"center\">", "");
                iTemp = iTemp.Replace("</td>", "");

                if (iTemp == "-")
                {
                    iRest[i] = 0;
                    iFirst[i] = "";
                    iSecond[i] = "";
                }
                else
                {
                    if (iTemp.Length == 4)
                    {
                        iRest[i] = float.Parse(iTemp);
                        iFirst[i] = "";
                        iSecond[i] = "";
                    }
                    else
                    {
                        iRest[i] = float.Parse(iTemp.Substring(0, 4));
                        iFirst[i] = iTemp.Substring(4, 1);
                        iSecond[i] = iTemp.Substring(5, 1);
                    }
                }


                i++;
                iResult = iResult.NextMatch();
            }

            //还有一个proceed 暂时不做处理

            iDetail.iRest = iRest;
            iDetail.iFirstDetail = iFirst;
            iDetail.iSecondDetail = iSecond;

            return iDetail;
        }

